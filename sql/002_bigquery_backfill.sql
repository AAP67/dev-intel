-- COST ESTIMATE:
--   GH Archive is ~2TB/year. Querying 2 years ≈ scanning 4TB.
--   BigQuery pricing: $6.25/TB = ~$25 total.
--   First 1TB/month is free, so first run might cost ~$19.
